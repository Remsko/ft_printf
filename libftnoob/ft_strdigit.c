#include "libft.h"

int	ft_strdigit(const char *s)
{
	int i;
	int mark;

	i = 0;
	mark = 0;
	while (s[i])
	{
		if (s[i] == '.')
		{
			i++;
			mark++;
		}
		if (!(s[i] >= '0' && s[i] <= '9') || mark > 1)
			return (0);
		i++;
	}
	return (1);
}
